package cn.zhuhongqing.lucene.domain;

public class Demand {

	private String demand_id;

	private String demand_title;

	private String demand_context;

	public String getDemand_id() {
		return demand_id;
	}

	public void setDemand_id(String demand_id) {
		this.demand_id = demand_id;
	}

	public String getDemand_title() {
		return demand_title;
	}

	public void setDemand_title(String demand_title) {
		this.demand_title = demand_title;
	}

	public String getDemand_context() {
		return demand_context;
	}

	public void setDemand_context(String demand_context) {
		this.demand_context = demand_context;
	}

}
